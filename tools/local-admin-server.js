const http = require("http");
const fs = require("fs");
const path = require("path");
const vm = require("vm");
const { spawnSync } = require("child_process");

const root = path.resolve(__dirname, "..");
const dataPath = path.join(root, "js", "data", "mods.js");
const imageDir = path.join(root, "images", "mods");
const port = Number(process.env.MODVAULT_ADMIN_PORT || 8787);

function send(res, status, data, type = "application/json") {
  const body = type === "application/json" ? JSON.stringify(data, null, 2) : data;
  res.writeHead(status, {
    "Content-Type": `${type}; charset=utf-8`,
    "Access-Control-Allow-Origin": "*",
    "Access-Control-Allow-Headers": "Content-Type",
    "Access-Control-Allow-Methods": "GET,POST,OPTIONS"
  });
  res.end(body);
}

function readBody(req, limit = 40 * 1024 * 1024) {
  return new Promise((resolve, reject) => {
    const chunks = [];
    let size = 0;
    req.on("data", chunk => {
      size += chunk.length;
      if (size > limit) {
        reject(new Error("Request is too large. Use up to 3 optimized images."));
        req.destroy();
        return;
      }
      chunks.push(chunk);
    });
    req.on("end", () => resolve(Buffer.concat(chunks).toString("utf8")));
    req.on("error", reject);
  });
}

function loadData() {
  const code = fs.readFileSync(dataPath, "utf8");
  const context = {};
  vm.createContext(context);
  vm.runInContext(`${code}
this.GAMES = GAMES;
this.MODS = MODS;
this.CATEGORIES = CATEGORIES;
this.SITE_NAME = typeof SITE_NAME === "undefined" ? "ModVault" : SITE_NAME;`, context);
  return {
    games: context.GAMES,
    mods: context.MODS,
    categories: context.CATEGORIES,
    siteName: context.SITE_NAME || "ModVault"
  };
}

function slugify(value) {
  return String(value || "mod")
    .normalize("NFKD")
    .replace(/[^\w\s-]/g, "")
    .trim()
    .toLowerCase()
    .replace(/[_\s]+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "") || "mod";
}

function escapeString(value) {
  return String(value ?? "")
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/\r?\n/g, " ");
}

function serializeObject(name, value) {
  return `const ${name} = ${JSON.stringify(value, null, 2)};`;
}

function serializeMod(mod) {
  const order = [
    "id",
    "game",
    "title",
    "category",
    "version",
    "size",
    "images",
    "image",
    "downloadUrl",
    "short",
    "description",
    "tags",
    "featured"
  ];
  const keys = [...order.filter(key => Object.prototype.hasOwnProperty.call(mod, key)), ...Object.keys(mod).filter(key => !order.includes(key))];
  const lines = ["  {"];
  for (const key of keys) {
    const value = mod[key];
    if (typeof value === "string") {
      lines.push(`    ${key}: "${escapeString(value)}",`);
    } else if (Array.isArray(value)) {
      lines.push(`    ${key}: ${JSON.stringify(value, null, 2).replace(/\n/g, "\n    ")},`);
    } else {
      lines.push(`    ${key}: ${JSON.stringify(value)},`);
    }
  }
  lines[lines.length - 1] = lines[lines.length - 1].replace(/,$/, "");
  lines.push("  }");
  return lines.join("\n");
}

function writeModsFile({ games, mods, categories, siteName }) {
  const text = [
    "// ============================================================",
    "//  mods.js - site mod database",
    "//  Generated by ModVault Local Admin.",
    "// ============================================================",
    "",
    serializeObject("GAMES", games),
    "",
    "const MODS = [",
    mods.map(serializeMod).join(",\n"),
    "];",
    "",
    serializeObject("CATEGORIES", categories),
    "",
    `const SITE_NAME = "${escapeString(siteName || "ModVault")}";`,
    ""
  ].join("\n");
  fs.writeFileSync(dataPath, text, "utf8");
}

function uniqueImageName(baseName, index) {
  const stem = slugify(baseName);
  let name = `${stem}-${index}.jpg`;
  let counter = 2;
  while (fs.existsSync(path.join(imageDir, name))) {
    name = `${stem}-${index}-${counter}.jpg`;
    counter += 1;
  }
  return name;
}

function saveImages(title, images) {
  fs.mkdirSync(imageDir, { recursive: true });
  return (images || []).slice(0, 3).map((image, index) => {
    const match = String(image.dataUrl || "").match(/^data:image\/(?:jpeg|jpg);base64,(.+)$/i);
    if (!match) {
      throw new Error(`Image ${index + 1} is not a JPEG data URL. Re-select it in the local admin.`);
    }
    const name = uniqueImageName(title, index + 1);
    fs.writeFileSync(path.join(imageDir, name), Buffer.from(match[1], "base64"));
    return `images/mods/${name}`;
  });
}

function normalizeTags(tags) {
  if (Array.isArray(tags)) return tags.map(tag => String(tag).trim().toLowerCase()).filter(Boolean);
  return String(tags || "")
    .split(",")
    .map(tag => tag.trim().toLowerCase())
    .filter(Boolean);
}

function runGenerator(script) {
  const result = spawnSync(process.execPath, [path.join(root, "tools", script)], {
    cwd: root,
    encoding: "utf8"
  });
  if (result.status !== 0) {
    throw new Error(`${script} failed: ${result.stderr || result.stdout}`);
  }
  return result.stdout.trim();
}

async function handleAddMod(req, res) {
  const payload = JSON.parse(await readBody(req));
  const data = loadData();
  const nextId = Math.max(0, ...data.mods.map(mod => Number(mod.id) || 0)) + 1;
  const id = Number(payload.id) || nextId;
  const title = String(payload.title || "").trim();
  if (!title) throw new Error("Title is required.");
  if (!data.games[payload.game]) throw new Error("Unknown game.");
  if (!data.categories[payload.game]?.[payload.category]) throw new Error("Unknown category for this game.");

  const savedImages = saveImages(title, payload.images);
  if (!savedImages.length) throw new Error("Add at least one image.");

  const mod = {
    id,
    game: payload.game,
    title,
    category: payload.category,
    version: String(payload.version || "1.0").trim(),
    size: String(payload.size || "").trim(),
    images: savedImages,
    downloadUrl: String(payload.downloadUrl || "").trim(),
    short: String(payload.short || "").trim(),
    description: String(payload.description || "").trim(),
    tags: normalizeTags(payload.tags),
    featured: Boolean(payload.featured)
  };

  const updated = data.mods.filter(item => Number(item.id) !== id);
  updated.push(mod);
  updated.sort((a, b) => Number(a.id) - Number(b.id));
  writeModsFile({ ...data, mods: updated });

  const modPages = runGenerator("generate-mod-pages.js");
  let editorial = "";
  try {
    editorial = runGenerator("generate-editorial-content.js");
  } catch (error) {
    editorial = `Editorial generator skipped: ${error.message}`;
  }

  send(res, 200, {
    ok: true,
    mod,
    images: savedImages,
    output: [modPages, editorial].filter(Boolean)
  });
}

function stripBBCode(text) {
  return String(text || "")
    .replace(/\[img[^\]]*\][\s\S]*?\[\/img\]/gi, "")
    .replace(/\[url=[^\]]*\]([\s\S]*?)\[\/url\]/gi, "$1")
    .replace(/\[color=[^\]]*\]([\s\S]*?)\[\/color\]/gi, "$1")
    .replace(/\[size=[^\]]*\]([\s\S]*?)\[\/size\]/gi, "$1")
    .replace(/\[font=[^\]]*\]([\s\S]*?)\[\/font\]/gi, "$1")
    .replace(/\[b\]([\s\S]*?)\[\/b\]/gi, "$1")
    .replace(/\[i\]([\s\S]*?)\[\/i\]/gi, "$1")
    .replace(/\[u\]([\s\S]*?)\[\/u\]/gi, "$1")
    .replace(/\[s\]([\s\S]*?)\[\/s\]/gi, "$1")
    .replace(/\[center\]([\s\S]*?)\[\/center\]/gi, "$1")
    .replace(/\[heading\]([\s\S]*?)\[\/heading\]/gi, "$1\n")
    .replace(/\[h[1-6]\]([\s\S]*?)\[\/h[1-6]\]/gi, "$1\n")
    .replace(/\[quote[^\]]*\]([\s\S]*?)\[\/quote\]/gi, "$1")
    .replace(/\[spoiler[^\]]*\]([\s\S]*?)\[\/spoiler\]/gi, "$1")
    .replace(/\[code[^\]]*\]([\s\S]*?)\[\/code\]/gi, "")
    .replace(/\[\*\]/g, "\n- ")
    .replace(/\[list[^\]]*\]/gi, "")
    .replace(/\[\/list\]/gi, "")
    .replace(/\[line\]/gi, "\n")
    .replace(/\[[^\]]*\]/g, "")
    .replace(/&amp;/g, "&").replace(/&lt;/g, "<").replace(/&gt;/g, ">").replace(/&quot;/g, '"').replace(/&#39;/g, "'")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

async function handleNexus(req, res) {
  const payload = JSON.parse(await readBody(req, 512 * 1024));
  const url = String(payload.url || "").trim();
  const apiKey = String(payload.apiKey || process.env.NEXUS_API_KEY || "").trim();
  if (!apiKey) throw new Error("NexusMods API key required. Get it at nexusmods.com → Settings → API Keys.");

  const match = url.match(/nexusmods\.com\/([^\/?\s]+)\/mods\/(\d+)/i);
  if (!match) throw new Error("Invalid NexusMods URL. Expected: nexusmods.com/{game}/mods/{id}");
  const [, gameDomain, modId] = match;

  const response = await fetch(`https://api.nexusmods.com/v1/games/${gameDomain}/mods/${modId}.json`, {
    headers: { apikey: apiKey, "User-Agent": "ModVault-LocalAdmin/1.0", Accept: "application/json" }
  });
  if (!response.ok) {
    const err = await response.json().catch(() => ({}));
    throw new Error(`NexusMods API ${response.status}: ${err.message || response.statusText}`);
  }
  const mod = await response.json();

  // NexusMods exposes only the main picture through its API; the gallery is
  // behind Cloudflare (page scraping returns 403). So we auto-grab the main
  // image and let the user paste extra gallery image URLs (right-click "Copy
  // image address" on the mod page), which we download from staticdelivery.
  const imageUrls = [];
  if (mod.picture_url) imageUrls.push(mod.picture_url);
  // The bookmarklet sends every staticdelivery URL it finds on the page; keep
  // only this mod's own gallery (path contains /images/{modId}/) and drop the
  // blurred variants, so site banners/badges never slip in. Doing this here
  // (not in the bookmarklet) means filter tweaks never need a re-drag.
  for (const raw of Array.isArray(payload.extraUrls) ? payload.extraUrls : []) {
    const u = String(raw || "").trim();
    if (!/^https:\/\/staticdelivery\.nexusmods\.com\//i.test(u)) continue;
    if (u.indexOf(`/images/${modId}/`) === -1) continue;
    if (/_blurred/i.test(u)) continue;
    if (!imageUrls.includes(u)) imageUrls.push(u);
  }

  async function fetchImageBase64(imgUrl) {
    try {
      const r = await fetch(imgUrl, {
        headers: { "User-Agent": "ModVault-LocalAdmin/1.0", "Referer": "https://www.nexusmods.com/" },
        signal: AbortSignal.timeout(15000)
      });
      if (!r.ok) return null;
      const buf = Buffer.from(await r.arrayBuffer());
      const ct = r.headers.get("content-type") || "image/jpeg";
      if (!ct.startsWith("image/")) return null;
      return `data:${ct};base64,${buf.toString("base64")}`;
    } catch (_) { return null; }
  }

  const images = (await Promise.all(imageUrls.slice(0, 3).map(fetchImageBase64))).filter(Boolean);

  const rawVersion = String(mod.version || "").trim();
  const version = (rawVersion && rawVersion.toLowerCase() !== "n/a" && rawVersion !== "0") ? rawVersion : "1.0";

  send(res, 200, {
    ok: true,
    name: mod.name,
    summary: mod.summary,
    description: stripBBCode(mod.description),
    version,
    category_name: mod.category_name || "",
    gameDomain,
    images
  });
}

async function handleAi(req, res) {
  const payload = JSON.parse(await readBody(req, 1024 * 1024));
  const apiKey = String(payload.apiKey || process.env.GEMINI_API_KEY || "").trim();
  if (!apiKey) throw new Error("Add a Google AI Studio API key or set GEMINI_API_KEY.");

  const prompt = String(payload.prompt || "");
  const models = ["gemini-2.5-flash", "gemini-2.0-flash", "gemini-2.5-flash-lite"];
  let lastError = null;

  for (const model of models) {
    const response = await fetch(`https://generativelanguage.googleapis.com/v1beta/models/${model}:generateContent?key=${encodeURIComponent(apiKey)}`, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ contents: [{ parts: [{ text: prompt }] }] })
    });
    const data = await response.json();
    if (!data.error) {
      send(res, 200, {
        ok: true,
        model,
        text: data.candidates?.[0]?.content?.parts?.[0]?.text || ""
      });
      return;
    }
    lastError = data.error;
    // 404 = model name not available, 429 = quota exhausted for this model.
    // Both are worth retrying on the next model (each has its own free quota);
    // any other error (e.g. bad key) is fatal, so stop.
    if (data.error.code !== 404 && data.error.code !== 429) break;
  }

  throw new Error(lastError ? `${lastError.message} (${lastError.code})` : "Gemini did not return text.");
}

// In-memory hand-off for the modland userscript. modland.net is fully behind
// Cloudflare, so the server can't scrape it; instead the userscript (running in
// the user's browser, which passed the challenge) downloads the images itself
// and POSTs the scraped mod here. The admin page then pulls it once and clears it.
let modlandStash = null;

async function handleStashPost(req, res) {
  const payload = JSON.parse(await readBody(req));
  modlandStash = {
    game: String(payload.game || "").trim(),
    title: String(payload.title || "").trim(),
    description: String(payload.description || "").trim(),
    images: (Array.isArray(payload.images) ? payload.images : [])
      .filter(img => typeof img === "string" && img.startsWith("data:image/"))
      .slice(0, 3),
    sourceUrl: String(payload.sourceUrl || "").trim()
  };
  send(res, 200, { ok: true, images: modlandStash.images.length });
}

function handleStashGet(req, res) {
  const stash = modlandStash;
  modlandStash = null;
  send(res, 200, { ok: true, stash });
}

function serveStatic(req, res) {
  const url = new URL(req.url, `http://localhost:${port}`);
  const requested = url.pathname === "/" ? "/local-admin.html" : decodeURIComponent(url.pathname);
  const file = path.resolve(root, requested.replace(/^\/+/, ""));
  if (!file.startsWith(root)) {
    send(res, 403, "Forbidden", "text/plain");
    return;
  }
  if (!fs.existsSync(file) || fs.statSync(file).isDirectory()) {
    send(res, 404, "Not found", "text/plain");
    return;
  }
  const ext = path.extname(file).toLowerCase();
  const types = {
    ".html": "text/html",
    ".css": "text/css",
    ".js": "text/javascript",
    ".svg": "image/svg+xml",
    ".ico": "image/x-icon",
    ".jpg": "image/jpeg",
    ".jpeg": "image/jpeg",
    ".png": "image/png",
    ".webp": "image/webp"
  };
  send(res, 200, fs.readFileSync(file), types[ext] || "application/octet-stream");
}

const server = http.createServer(async (req, res) => {
  try {
    if (req.method === "OPTIONS") {
      send(res, 204, {});
      return;
    }
    if (req.method === "GET" && req.url === "/api/data") {
      const data = loadData();
      send(res, 200, {
        games: data.games,
        categories: data.categories,
        nextId: Math.max(0, ...data.mods.map(mod => Number(mod.id) || 0)) + 1,
        modCount: data.mods.length
      });
      return;
    }
    if (req.method === "POST" && req.url === "/api/add-mod") {
      await handleAddMod(req, res);
      return;
    }
    if (req.method === "POST" && req.url === "/api/ai") {
      await handleAi(req, res);
      return;
    }
    if (req.method === "POST" && req.url === "/api/nexus") {
      await handleNexus(req, res);
      return;
    }
    if (req.method === "POST" && req.url === "/api/stash") {
      await handleStashPost(req, res);
      return;
    }
    if (req.method === "GET" && req.url === "/api/stash") {
      handleStashGet(req, res);
      return;
    }
    if (req.method === "GET") {
      serveStatic(req, res);
      return;
    }
    send(res, 405, { error: "Method not allowed" });
  } catch (error) {
    send(res, 500, { ok: false, error: error.message });
  }
});

server.listen(port, () => {
  console.log(`ModVault Local Admin is running: http://localhost:${port}/local-admin.html`);
  console.log(`Project folder: ${root}`);
});
